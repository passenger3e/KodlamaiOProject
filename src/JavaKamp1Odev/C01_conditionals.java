package JavaKamp1Odev;

public class C01_conditionals {
    public static void main(String[] args) {
        int number=20;
        if(number<20){
            System.out.println("number is less then 20");
        }else if(number==20){
            System.out.println("number is equal to 20");
        }else{
            System.out.println("number is bigger then 20");
        }
    }
}
